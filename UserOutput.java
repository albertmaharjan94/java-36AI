public class UserOutput{
    public static void main(String[] args){
        // Print continuosly using print()
        System.out.print("This");
        System.out.print("is");
        System.out.print("Java\n");
        // Print with line break
        System.out.println("This line will break after this string");
        System.out.println("This will continue in next line");
        // Print with formatting using printf()
        // %d -> number, %f -> float/double, %s -> String or any
        System.out.printf("Name: %s, Age: %d, Temp: %f", "Mero Namm", 30, 22.22);
        // printf will not break line
        
    }
}