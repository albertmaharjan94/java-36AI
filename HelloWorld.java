public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World"); 
        // Double backslash is comment single line
        System.out.println("Next line");
        // extension sout for System.out.println
        /** Multi line comment
         * can be in multiple line
         * line number 8
         */
        System.out.println("Escape sequence.\nStart\"s in new");
        /** Print the following using one System.out.println()
         * Java Language is Object Oriented Language
         * It's feature are great 
         * It"s Platform Independent
         */
        System.out.println("Java Language is Object Oriented Language\nIt\'s feature are great.\nIt\"s Platform Independent");
    }
}